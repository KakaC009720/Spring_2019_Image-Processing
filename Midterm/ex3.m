c=imread('cameraman.png');
f1=fspecial('average');
f2=fspecial('average',[11,11]);
f3=fspecial('average',[25,25]);
cf1=imfilter(c,f1);
cf2=imfilter(c,f2);
cf3=imfilter(c,f3);
sh=histeq(c);
sh1=histeq(cf1);
sh2=histeq(cf2);
sh3=histeq(cf3);
figure
subplot(2,2,1),imshow(sh);
subplot(2,2,2),imshow(sh1);
subplot(2,2,3),imshow(sh2);
subplot(2,2,4),imshow(sh3);
figure
